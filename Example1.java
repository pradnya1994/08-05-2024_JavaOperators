package com.javaBinaryOperators;

public class Example1 {

	public static void main(String[] args) {
		
		int value1 = 200, value2 = 20;
		
		//Arithmetic operators: +, -, *, / and %
		
		System.out.println("+: "+(value1+value2));
		System.out.println("-: "+(value1-value2));
		System.out.println("*: "+(value1*value2));
		System.out.println("/: "+(value1/value2));
		System.out.println("%: "+(value1%value2));
	}

}
